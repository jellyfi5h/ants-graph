#include "libft.h"

void	ft_dbl_strdel(char **str)
{
	int		i;

	i = 0;
	if (str != NULL)
	{
		while (str[i])
		{
			ft_strdel(&str[i]);
			i++;
		}
		free(str);
		str = NULL;
	}
}
