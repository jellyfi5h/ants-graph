#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	int		j;
	int		len;

	i = 0;
	j = 0;
	len = ft_strlen(dst);
	i = len;
	if ((int)size < len)
		return (size + ft_strlen(src));
	while (i < size - 1 && src[j] != '\0')
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (len + ft_strlen(src));
}
