#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	int		i;
	char	*str;

	if (s != NULL)
	{
		str = (char*)malloc(len + 1);
		if (str == NULL)
			return (NULL);
		i = 0;
		while (i < (int)len && s[start] != '\0')
		{
			str[i] = s[start];
			i++;
			start++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
