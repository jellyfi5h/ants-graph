#include "libft.h"

void	ft_putnbr(int n)
{
	char		arr[15];
	int			c;
	long	int	nbim;

	c = 0;
	nbim = n;
	if (nbim == 0)
		write(1, "0", 1);
	if (nbim < 0)
	{
		write(1, "-", 1);
		nbim *= (-1);
	}
	while (nbim != 0)
	{
		arr[c] = ((nbim % 10) + '0');
		nbim /= 10;
		c++;
	}
	while ((c - 1) >= 0)
	{
		write(1, &(arr[c - 1]), 1);
		c--;
	}
}
