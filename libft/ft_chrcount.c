#include "libft.h"

int		ft_chrcount(const char *str, char c)
{
	int		i;
	int		len;

	len = 0;
	i = -1;
	while (str[++i] != '\0')
	{
		if (str[i] == c)
			len++;
	}
	return (len);
}
